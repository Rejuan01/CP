void solve(){
    ll n, k;
    cin >> n >> k;
    ll ans = 0;
    if(n%2 == 1){
        n -= k;
        ans ++;
    }
    ans += (n/(k-1));
    if(n%(k-1) != 0) ans++;
    cout << ans << endl;
}
