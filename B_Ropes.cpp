void solve(){
    ll n, k;
    cin >> n >> k;
    vi a(n);
    for(ll i = 0; i < n; i++) cin >> a[i];
    double l = 0, r = 1e7+1;
    while(l+ 1e-6 < r){
        double m = (l+r)/2;
        ll cnt = 0;
        for(auto it: a) cnt += it/m;
        if(cnt >= k) l = m;
        else r = m;
    }
    cout << fixed << setprecision(7) << l << '\n';
}
